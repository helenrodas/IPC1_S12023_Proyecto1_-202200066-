    private void CargarRegiones(){
       String Datos[][] = new String[listaRegiones.size()][100];
    
        for (int i = 0; i < listaRegiones.size(); i++) {
            
            Datos[i][0]=listaRegiones.get(i).getCodigo();
            Datos[i][1]=listaRegiones.get(i).getNombre();
            Datos[i][2]=listaRegiones.get(i).getPrecioEstandar().toString();
            Datos[i][3]=listaRegiones.get(i).getPrecioEspecial().toString();
        }
        jTableRegiones.setModel(new javax.swing.table.DefaultTableModel(Datos, new String [] {"Codigo","Nombre","Precio Estandard","Precio Especial"}) );
    }
    

     

