    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        if( txtPrecioEstandar.getText().trim().length() == 0 || 
                txtPrecioEspecial.getText().trim().length() == 0 ){
            JOptionPane.showMessageDialog( this,  "Valores de precios no pueden estar vacios.");
            return;
        }
        
        String precioEstandardAsString = txtPrecioEstandar.getText();
        Double precioEstandarAsDouble = Double.parseDouble(precioEstandardAsString);
 
        String precioEspecialAsString = txtPrecioEspecial.getText();
        Double precioEpecialAsDouble = Double.parseDouble(precioEspecialAsString);
        
        String currentRegion = txtRegion.getText();
       // String regionNombre;
        data.actualizarTarifas(currentRegion, precioEpecialAsDouble, precioEstandarAsDouble);
        
        limpiarCampos();
        CargarRegiones();
    }//GEN-LAST:event_btnActualizarActionPerformed

