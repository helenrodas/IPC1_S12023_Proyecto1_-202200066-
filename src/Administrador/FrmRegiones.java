    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
         String precioEstandarAsString = txtPrecioEstandar.getText().trim();
         String precioEspecialAsString = txtPrecioEspecial.getText().trim();
         
           if( precioEstandarAsString.isBlank() || precioEspecialAsString.isBlank()){
                JOptionPane.showMessageDialog(this,  "No hay datos para eliminar");
                txtPrecioEstandar.setText("");
                txtPrecioEspecial.setText("");
                return;
            }
    
        Double precioEstandar =Double.parseDouble(txtPrecioEstandar.getText()) ;
        Double precioEspecial =Double.parseDouble(txtPrecioEspecial.getText()) ;
        
        data.eliminarTarifas(precioEstandar, precioEspecial);
        
        limpiarCampos();
        CargarRegiones();
    }//GEN-LAST:event_btnEliminarActionPerformed

