    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nombreKiosco = txtNombreKiosco.getText().trim();
        String codigoKiosco = txtCodigoKiosco.getText().trim();
        String regionKiosco=comboBoxRegionKiosco.getSelectedItem().toString(); 
        
        if( nombreKiosco.isBlank() || 
            codigoKiosco.isBlank() || 
            regionKiosco.contentEquals("-Seleccione Region-") ){
            JOptionPane.showMessageDialog( this,  "Todos los campos son obligatorios");
            limpiarCampos();
            return;
        }
        
       if( data.existeKiosco(codigoKiosco, nombreKiosco)){
           JOptionPane.showMessageDialog( this,  "El Kiosco ya existe!");
            LimpiarFormulario();
       }
       else{
           CKiosco kiosco = new CKiosco( codigoKiosco, nombreKiosco,regionKiosco);
     
            listaKioscos.add(kiosco);
            
            mostrarDatos();
            LimpiarFormulario();
       }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

