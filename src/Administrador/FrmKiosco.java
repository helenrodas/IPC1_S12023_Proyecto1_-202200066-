    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String nombreKiosco = txtNombreKiosco.getText().trim();
        String codigoKiosco = txtCodigoKiosco.getText().trim();
        String regionKiosco=comboBoxRegionKiosco.getSelectedItem().toString();
        
        if( nombreKiosco.isBlank() || 
            codigoKiosco.isBlank() || 
            regionKiosco.contentEquals("-Seleccione Region-") ){
            JOptionPane.showMessageDialog( this,  "No hay datos para eliminar");
            txtCodigoKiosco.setText("");
            txtNombreKiosco.setText("");
            comboBoxRegionKiosco.requestFocus();
            return;
        }
        
        data.eliminarKiosco(codigoKiosco);
        txtCodigoKiosco.setEditable(true);

        limpiarCampos();
        mostrarDatos();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        String codigoKiosco = txtCodigoKiosco.getText().trim();
        String nombreKiosco = txtNombreKiosco.getText().trim();
        String nombreRegion = comboBoxRegionKiosco.getSelectedItem().toString();
        
        data.actualizarKiosco(codigoKiosco, nombreKiosco, nombreRegion);
        txtCodigoKiosco.setEditable(true);
        
        limpiarCampos();
        mostrarDatos();
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void limpiarCampos(){
        txtCodigoKiosco.setText("");
        txtNombreKiosco.setText("");
        comboBoxRegionKiosco.setSelectedItem("-Seleccione Region-");
    }
    
    private void jTableKioscosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableKioscosMouseClicked
        JTable table = (JTable) evt.getSource();
        Point point = evt.getPoint();
        int row = table.rowAtPoint(point);
        
        txtCodigoKiosco.setText(listaKioscos.get(row).getCodigoKiosco());
        txtNombreKiosco.setText ( listaKioscos.get(row).getNombreKiosco()); 
        comboBoxRegionKiosco.setSelectedItem(listaKioscos.get(row).getRegionKiosco());
    }//GEN-LAST:event_jTableKioscosMouseClicked

