    public void mostrarDatos(){
        String GuardarDatos[][] = new String[listaKioscos.size()][20];
    
        for (int i = 0; i < listaKioscos.size(); i++) {
            
            GuardarDatos[i][0]=listaKioscos.get(i).getCodigoKiosco();
            GuardarDatos[i][1]=listaKioscos.get(i).getNombreKiosco();
            GuardarDatos[i][2]=listaKioscos.get(i).getRegionKiosco();
        }
        jTableKioscos.setModel(new javax.swing.table.DefaultTableModel(GuardarDatos, new String [] {"Codigo","Nombre","Region"}) );
    } 
    
    public void LimpiarFormulario(){
        txtCodigoKiosco.setText("");
        txtNombreKiosco.setText("");
        comboBoxRegionKiosco.setSelectedIndex(0);
    }
        
